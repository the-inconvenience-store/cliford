package cli

import (
	"os"
	"path/filepath"
)

// ConfigCmdGenerator produces config management commands for the generated app.
type ConfigCmdGenerator struct {
	outputDir string
	appName   string
}

// NewConfigCmdGenerator creates a config command generator.
func NewConfigCmdGenerator(outputDir, appName string) *ConfigCmdGenerator {
	return &ConfigCmdGenerator{outputDir: outputDir, appName: appName}
}

// Generate produces the config command file for the generated app.
func (g *ConfigCmdGenerator) Generate() error {
	cliDir := filepath.Join(g.outputDir, "internal", "cli")
	if err := os.MkdirAll(cliDir, 0o755); err != nil {
		return err
	}

	var sb StringBuilder
	sb.Line("// Code generated by Cliford. DO NOT EDIT outside custom code regions.")
	sb.Line("package cli")
	sb.Line("")
	sb.Line("import (")
	sb.Line(`	"fmt"`)
	sb.Line(`	"net/url"`)
	sb.Line(`	"os"`)
	sb.Line(`	"os/exec"`)
	sb.Line(`	"path/filepath"`)
	sb.Line(`	"sort"`)
	sb.Line(`	"time"`)
	sb.Line("")
	sb.Line(`	"github.com/spf13/cobra"`)
	sb.Line(`	"github.com/spf13/viper"`)
	sb.Line(`	"gopkg.in/yaml.v3"`)
	sb.Line(")")
	sb.Line("")

	// Config parent command
	sb.Line("func configCmd() *cobra.Command {")
	sb.Line("	cmd := &cobra.Command{")
	sb.Line(`		Use:   "config",`)
	sb.Line(`		Short: "Manage app configuration",`)
	sb.Line("	}")
	sb.Line("")
	sb.Line("	cmd.AddCommand(configShowCmd())")
	sb.Line("	cmd.AddCommand(configSetCmd())")
	sb.Line("	cmd.AddCommand(configGetCmd())")
	sb.Line("	cmd.AddCommand(configResetCmd())")
	sb.Line("	cmd.AddCommand(configEditCmd())")
	sb.Line("	cmd.AddCommand(configPathCmd())")
	sb.Line("	cmd.AddCommand(configValidateCmd())")
	sb.Line("	cmd.AddCommand(configMigrateCmd())")
	sb.Line("")
	sb.Line("	return cmd")
	sb.Line("}")
	sb.Line("")

	// Show
	sb.Line("func configShowCmd() *cobra.Command {")
	sb.Line("	return &cobra.Command{")
	sb.Line(`		Use:   "show",`)
	sb.Line(`		Short: "Display current configuration",`)
	sb.Line("		RunE: func(cmd *cobra.Command, args []string) error {")
	sb.Line("			settings := viper.AllSettings()")
	sb.Line("			return FormatOutput(settings, outputFormat)")
	sb.Line("		},")
	sb.Line("	}")
	sb.Line("}")
	sb.Line("")

	// Set
	sb.Line("func configSetCmd() *cobra.Command {")
	sb.Line("	return &cobra.Command{")
	sb.Line(`		Use:   "set [key] [value]",`)
	sb.Line(`		Short: "Set a configuration value",`)
	sb.Line(`		Args:  cobra.ExactArgs(2),`)
	sb.Line("		RunE: func(cmd *cobra.Command, args []string) error {")
	sb.Line("			viper.Set(args[0], args[1])")
	sb.Line("			configDir, _ := os.UserConfigDir()")
	sb.Linef("			configPath := filepath.Join(configDir, %q, \"config.yaml\")", g.appName)
	sb.Line("			if err := viper.WriteConfigAs(configPath); err != nil {")
	sb.Line("				return err")
	sb.Line("			}")
	sb.Line(`			fmt.Printf("Set %s = %s\n", args[0], args[1])`)
	sb.Line("			return nil")
	sb.Line("		},")
	sb.Line("	}")
	sb.Line("}")
	sb.Line("")

	// Get
	sb.Line("func configGetCmd() *cobra.Command {")
	sb.Line("	return &cobra.Command{")
	sb.Line(`		Use:   "get [key]",`)
	sb.Line(`		Short: "Get a configuration value",`)
	sb.Line(`		Args:  cobra.ExactArgs(1),`)
	sb.Line("		RunE: func(cmd *cobra.Command, args []string) error {")
	sb.Line("			val := viper.Get(args[0])")
	sb.Line("			if val == nil {")
	sb.Line(`				return fmt.Errorf("key %q not found", args[0])`)
	sb.Line("			}")
	sb.Line(`			fmt.Println(val)`)
	sb.Line("			return nil")
	sb.Line("		},")
	sb.Line("	}")
	sb.Line("}")
	sb.Line("")

	// Reset
	sb.Line("func configResetCmd() *cobra.Command {")
	sb.Line("	return &cobra.Command{")
	sb.Line(`		Use:   "reset",`)
	sb.Line(`		Short: "Reset configuration to defaults",`)
	sb.Line("		RunE: func(cmd *cobra.Command, args []string) error {")
	sb.Line("			configDir, _ := os.UserConfigDir()")
	sb.Linef("			configPath := filepath.Join(configDir, %q, \"config.yaml\")", g.appName)
	sb.Line("			if err := os.Remove(configPath); err != nil && !os.IsNotExist(err) {")
	sb.Line("				return err")
	sb.Line("			}")
	sb.Line(`			fmt.Println("Configuration reset to defaults")`)
	sb.Line("			return nil")
	sb.Line("		},")
	sb.Line("	}")
	sb.Line("}")
	sb.Line("")

	// Edit
	sb.Line("func configEditCmd() *cobra.Command {")
	sb.Line("	return &cobra.Command{")
	sb.Line(`		Use:   "edit",`)
	sb.Line(`		Short: "Open config file in $EDITOR",`)
	sb.Line("		RunE: func(cmd *cobra.Command, args []string) error {")
	sb.Line(`			editor := os.Getenv("EDITOR")`)
	sb.Line(`			if editor == "" {`)
	sb.Line(`				editor = "vi"`)
	sb.Line("			}")
	sb.Line("			configDir, _ := os.UserConfigDir()")
	sb.Linef("			configPath := filepath.Join(configDir, %q, \"config.yaml\")", g.appName)
	sb.Line("			c := exec.Command(editor, configPath)")
	sb.Line("			c.Stdin = os.Stdin")
	sb.Line("			c.Stdout = os.Stdout")
	sb.Line("			c.Stderr = os.Stderr")
	sb.Line("			return c.Run()")
	sb.Line("		},")
	sb.Line("	}")
	sb.Line("}")
	sb.Line("")

	// Path
	sb.Line("func configPathCmd() *cobra.Command {")
	sb.Line("	return &cobra.Command{")
	sb.Line(`		Use:   "path",`)
	sb.Line(`		Short: "Show config file location",`)
	sb.Line("		Run: func(cmd *cobra.Command, args []string) {")
	sb.Line("			configDir, _ := os.UserConfigDir()")
	sb.Linef("			fmt.Println(filepath.Join(configDir, %q, \"config.yaml\"))", g.appName)
	sb.Line("		},")
	sb.Line("	}")
	sb.Line("}")

	// Validate
	sb.Line("func configValidateCmd() *cobra.Command {")
	sb.Line("	var strict bool")
	sb.Line("	cmd := &cobra.Command{")
	sb.Line(`		Use:   "validate",`)
	sb.Line(`		Short: "Check the configuration for errors",`)
	sb.Line(`		Long:  "Validate configuration values for correct types, formats, and cross-references. Exits non-zero if any errors are found.",`)
	sb.Line("		RunE: func(cmd *cobra.Command, args []string) error {")
	sb.Line("			var errs []string")
	sb.Line("			var warns []string")
	sb.Line("")
	sb.Line("			// server_url must be a valid http or https URL")
	sb.Line(`			if serverURL := viper.GetString("server_url"); serverURL != "" {`)
	sb.Line("				parsed, err := url.Parse(serverURL)")
	sb.Line(`				if err != nil || (parsed.Scheme != "http" && parsed.Scheme != "https") {`)
	sb.Line(`					errs = append(errs, fmt.Sprintf("server_url: %q must be a valid http or https URL", serverURL))`)
	sb.Line("				}")
	sb.Line("			}")
	sb.Line("")
	sb.Line("			// request_timeout must be a parseable Go duration string")
	sb.Line(`			if raw := viper.GetString("request_timeout"); raw != "" {`)
	sb.Line(`				if _, err := time.ParseDuration(raw); err != nil {`)
	sb.Line(`					errs = append(errs, fmt.Sprintf("request_timeout: invalid duration %q", raw))`)
	sb.Line("				}")
	sb.Line("			}")
	sb.Line("")
	sb.Line("			// features.retry.max_attempts must be a positive integer")
	sb.Line(`			if viper.IsSet("features.retry.max_attempts") {`)
	sb.Line(`				if n := viper.GetInt("features.retry.max_attempts"); n <= 0 {`)
	sb.Line(`					errs = append(errs, fmt.Sprintf("features.retry.max_attempts: must be a positive integer, got %d", n))`)
	sb.Line("				}")
	sb.Line("			}")
	sb.Line("")
	sb.Line("			// features.retry.initial_interval must be a parseable Go duration string")
	sb.Line(`			if raw := viper.GetString("features.retry.initial_interval"); raw != "" {`)
	sb.Line(`				if _, err := time.ParseDuration(raw); err != nil {`)
	sb.Line(`					errs = append(errs, fmt.Sprintf("features.retry.initial_interval: invalid duration %q", raw))`)
	sb.Line("				}")
	sb.Line("			}")
	sb.Line("")
	sb.Line("			// active_profile must reference a defined profile when profiles are configured")
	sb.Line(`			if active := viper.GetString("active_profile"); active != "" {`)
	sb.Line(`				if profiles := viper.GetStringMap("profiles"); len(profiles) > 0 {`)
	sb.Line("					if _, ok := profiles[active]; !ok {")
	sb.Line(`						warns = append(warns, fmt.Sprintf("active_profile %q is not defined under \"profiles\"", active))`)
	sb.Line("					}")
	sb.Line("				}")
	sb.Line("			}")
	sb.Line("")
	sb.Line("			// Warn on unrecognised top-level keys (likely typos)")
	sb.Line("			known := map[string]bool{")
	sb.Line(`				"server_url": true, "request_timeout": true, "features": true,`)
	sb.Line(`				"global_params": true, "active_profile": true, "profiles": true,`)
	sb.Line(`				"auth": true, "config_version": true,`)
	sb.Line("			}")
	sb.Line("			var unknown []string")
	sb.Line("			for k := range viper.AllSettings() {")
	sb.Line("				if !known[k] {")
	sb.Line("					unknown = append(unknown, k)")
	sb.Line("				}")
	sb.Line("			}")
	sb.Line("			sort.Strings(unknown)")
	sb.Line("			for _, k := range unknown {")
	sb.Line(`				warns = append(warns, fmt.Sprintf("unknown key %q (possible typo?)", k))`)
	sb.Line("			}")
	sb.Line("")
	sb.Line("			for _, w := range warns {")
	sb.Line(`				fmt.Fprintf(os.Stderr, "warning: %s\n", w)`)
	sb.Line("			}")
	sb.Line("			if len(errs) == 0 && (!strict || len(warns) == 0) {")
	sb.Line(`				fmt.Println("Configuration is valid.")`)
	sb.Line("				return nil")
	sb.Line("			}")
	sb.Line("			for _, e := range errs {")
	sb.Line(`				fmt.Fprintf(os.Stderr, "error: %s\n", e)`)
	sb.Line("			}")
	sb.Line("			total := len(errs)")
	sb.Line("			if strict {")
	sb.Line("				total += len(warns)")
	sb.Line("			}")
	sb.Line(`			return fmt.Errorf("configuration has %d issue(s)", total)`)
	sb.Line("		},")
	sb.Line("	}")
	sb.Line(`	cmd.Flags().BoolVar(&strict, "strict", false, "Treat warnings as errors")`)
	sb.Line("	return cmd")
	sb.Line("}")
	sb.Line("")

	// Migrate
	sb.Line("func configMigrateCmd() *cobra.Command {")
	sb.Line("	var dryRun bool")
	sb.Line("	cmd := &cobra.Command{")
	sb.Line(`		Use:   "migrate",`)
	sb.Line(`		Short: "Upgrade the config file to the current schema version",`)
	sb.Line(`		Long:  "Detect the config schema version and apply any needed migrations. A .bak backup is written before modifying the file.",`)
	sb.Line("		RunE: func(cmd *cobra.Command, args []string) error {")
	sb.Line("			configDir, _ := os.UserConfigDir()")
	sb.Linef("			configPath := filepath.Join(configDir, %q, \"config.yaml\")", g.appName)
	sb.Line(`			const currentVersion = "1"`)
	sb.Line("")
	sb.Line("			data, err := os.ReadFile(configPath)")
	sb.Line("			if err != nil {")
	sb.Line("				if os.IsNotExist(err) {")
	sb.Line(`					fmt.Println("No config file found; nothing to migrate.")`)
	sb.Line("					return nil")
	sb.Line("				}")
	sb.Line(`				return fmt.Errorf("read config: %w", err)`)
	sb.Line("			}")
	sb.Line("")
	sb.Line("			var raw map[string]any")
	sb.Line("			if err := yaml.Unmarshal(data, &raw); err != nil {")
	sb.Line(`				return fmt.Errorf("parse config: %w", err)`)
	sb.Line("			}")
	sb.Line("			if raw == nil {")
	sb.Line("				raw = map[string]any{}")
	sb.Line("			}")
	sb.Line("")
	sb.Line(`			fileVersion, _ := raw["config_version"].(string)`)
	sb.Line("			if fileVersion == currentVersion {")
	sb.Line(`				fmt.Printf("Config is already at version %s.\n", currentVersion)`)
	sb.Line("				return nil")
	sb.Line("			}")
	sb.Line(`			if fileVersion != "" && fileVersion > currentVersion {`)
	sb.Line(`				fmt.Fprintf(os.Stderr, "warning: config_version %q is newer than supported (%s) — not migrating\n", fileVersion, currentVersion)`)
	sb.Line("				return nil")
	sb.Line("			}")
	sb.Line("")
	sb.Line("			var changes []string")
	sb.Line(`			if fileVersion == "" {`)
	sb.Line(`				changes = append(changes, fmt.Sprintf("add config_version = %q", currentVersion))`)
	sb.Line("			}")
	sb.Line("			if len(changes) == 0 {")
	sb.Line(`				fmt.Println("No migrations needed.")`)
	sb.Line("				return nil")
	sb.Line("			}")
	sb.Line("")
	sb.Line(`			fmt.Println("Pending migrations:")`)
	sb.Line("			for _, c := range changes {")
	sb.Line(`				fmt.Printf("  - %s\n", c)`)
	sb.Line("			}")
	sb.Line("			if dryRun {")
	sb.Line(`				fmt.Println("(dry-run: no changes written)")`)
	sb.Line("				return nil")
	sb.Line("			}")
	sb.Line("")
	sb.Line("			backupPath := configPath + \".bak\"")
	sb.Line("			if err := os.WriteFile(backupPath, data, 0o600); err != nil {")
	sb.Line(`				return fmt.Errorf("write backup %s: %w", backupPath, err)`)
	sb.Line("			}")
	sb.Line(`			fmt.Printf("Backup written to %s\n", backupPath)`)
	sb.Line("")
	sb.Line(`			raw["config_version"] = currentVersion`)
	sb.Line("			out, err := yaml.Marshal(raw)")
	sb.Line("			if err != nil {")
	sb.Line(`				return fmt.Errorf("marshal migrated config: %w", err)`)
	sb.Line("			}")
	sb.Line("			if err := os.MkdirAll(filepath.Dir(configPath), 0o755); err != nil {")
	sb.Line(`				return fmt.Errorf("create config dir: %w", err)`)
	sb.Line("			}")
	sb.Line("			if err := os.WriteFile(configPath, out, 0o600); err != nil {")
	sb.Line(`				return fmt.Errorf("write migrated config: %w", err)`)
	sb.Line("			}")
	sb.Line(`			fmt.Printf("Config migrated to version %s.\n", currentVersion)`)
	sb.Line("			return nil")
	sb.Line("		},")
	sb.Line("	}")
	sb.Line(`	cmd.Flags().BoolVar(&dryRun, "dry-run", false, "Show what would change without writing")`)
	sb.Line("	return cmd")
	sb.Line("}")

	return writeFormatted(filepath.Join(cliDir, "config_cmd.go"), sb.String())
}
